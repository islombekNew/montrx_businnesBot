export function getText(lang = 'uz', key, vars = {}) {
  let text = locales[lang]?.[key] || locales['uz']?.[key] || key;
  Object.entries(vars).forEach(([k, v]) => { text = text.replace(`{${k}}`, v); });
  return text;
}

